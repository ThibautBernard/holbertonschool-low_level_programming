#include "3-calc.h"

/**
 * op_add - addition of two param
 * @a: number 1
 * @b: number 2
 * Return: the sum of the addition
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substrate of two param
 * @a: number 1
 * @b: number 2
 * Return: the sum of the sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication of two param
 * @a: number 1
 * @b: number 2
 * Return: the sum of the multip
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of two param
 * @a: number 1
 * @b: number 2
 * Return: the sum of the div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo of two param
 * @a: number 1
 * @b: number 2
 * Return: the sum of the modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
